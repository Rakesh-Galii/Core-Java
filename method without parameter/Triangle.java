class  Triangle
{
	static void area()
	{
		int b=10;
		int h=2;
		double res=0.5*b*h;
		System.out.println("area of triangle  " +res);
	}
	static void area1()
	{
		int w=10;
		int h=20;
		double res=w*h;
		System.out.println("area of rectangle  " +res);
	}
	static void area2()
	{
		int a=10;
		
		double res=a*a;
		System.out.println("area of square  " +res);
	}
	static void area3()
	{
		int b=10;
		int h=20;
		int res=b*h;
		System.out.println("area of parallelogram  " +res);
	}
	static void area4()
	{
		final double pi=3.142;
		int r=5;
		double res=pi*r*r;
		System.out.println("area of Circle  " +res);
	}
	static void area5()
	{
		int a=10;
		int b=20;
		int h=30;
		double res=0.5*(a+b)*h;
		System.out.println("area of trapezoid  " +res);
	}
	static void area6()
	{
		final double pi=3.142;
		int a=10;
		int b=2;
		double res=pi*a*b;
		System.out.println("area of ellipse  " +res);
	}
	static void area7()
	{
		int r=5;
		double t=30.5;
		double res=0.5*r*r*t;
		System.out.println("area of sector  " +res);
	}
	public static void main(String[] args) 
	{
		System.out.println("***********start main*************");
		area();
		area1();
		area2();
		area3();
		area4();
		area5();
		area6();
		area7();
		System.out.println("***********start end*************");
	}
}
